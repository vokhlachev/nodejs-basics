const os = require('os')

//Platform
console.log(os.platform())

//Architecture
console.log(os.arch())

//Information about processor
console.log(os.cpus())

//Free memory
console.log(os.freemem())

//Amount of memory at all
console.log(os.totalmem())

//Root directory
console.log(os.homedir())

//How long system work?
console.log(os.uptime())